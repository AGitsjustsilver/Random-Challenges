    public static void lSC(String s){
        // counts occurence
        boolean occured;
        for (int i = 0; i < s.length(); i++) {
            occured = false;
            for (int g = 0; g < ALPH.length && !occured; g++) {
                if (s.charAt(i) == ALPH[g]) {
                    o[g] += 1;
                    occured = true;
                }
            }
        }
        //sorts the indexes
        for (int i = 0; i < o.length; i++) {
            int max = -1,maxIndex = 0;
            for (int g = 0; g < oIndex.length; g++) {
                if (o[g] > max) {
                   max = o[g];
                   maxIndex = g;
                }
            }
            o[maxIndex] = 0;
            oIndex[i] = maxIndex;
        }
        for (int a : oIndex) {
            end += ALPH[a];
        }
        boolean underScore = false;
        for (int i = end.length()-1; i >= 0 && !underScore; i--) {
            if(end.charAt(i) == '_'){
                System.out.println(end.substring(0,i));
                underScore = true;
            }
        }
    }

